#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		s;
	int		r;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		s = -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		r *= 10;
		r = r + (str[i] - 48);
		i++;
	}
	return (r * s);
}
