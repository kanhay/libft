#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	*p;
	int		i;

	p = (char *)str;
	i = 0;
	while (p[i])
	{
		if (p[i] == (char)c)
			return (&p[i]);
		i++;
	}
	if (p[i] == (char)c)
		return (&p[i]);
	return (0);
}
